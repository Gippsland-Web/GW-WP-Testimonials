<div class="wrap">
    <h1>GW Testimonial Options</h1>
    <p>The following shortcodes are available.</p>
    <h2 class="title">[testimonial-entry]</h2>
    <ul>
        <li><strong>template</strong>: Override template file name default "testimonial-entry.php"</li>
    </ul>
    <h2 class="title">[testimonials]</h2>
    <ul>
        <li><strong>template</strong>: Override template file name default "testimonial-archive.php"</li>
        <li><strong>numposts</strong>: Number of posts to show, default 5</li>
    </ul>
    <h2 class="title">[testimonial-single]</h2>
    <ul>
        <li><strong>template</strong>: Override template file name default "testimonial-single.php"</li>
        <li><strong>id</strong>: ID of the testimonial to show, default 0 and shows latest</li>
    </ul>
    <h2 class="title">[testimonial-slider]</h2>
    <ul>
        <li><strong>template</strong>: Override template file name default "testimonial-slider.php"</li>
        <li><strong>numposts</strong>: Number of posts to show, default 5</li>
        <li><strong>effect</strong>: Transition effect to use, default "pushInRight" Complete list <a href="http://webbies.dk/assets/files/SudoSlider/package/demos/all%20effects.html">available</a> </li>
        <li><strong>auto</strong>: Auto transition, default true</li>
        <li><strong>prevnext</strong>: Show Prev Next buttons, default false</li>
        <li><strong>pause</strong>: Delay between transitions, default 3000</li>

    </ul>
    <hr>
    <h1>Customization</h1>
    <p>The templates folder can be copied into your child themes folder if you wish to modify the html of the widgets without modifying the plugin directly.</p>

